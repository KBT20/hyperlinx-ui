export const vendorRegistryDiagnostics: readonly VendorDiagnostic[] = Object.freeze(
  vendorProfiles.flatMap((vendor) => [
    createVendorDiagnostic("VENDOR_PROFILE_REGISTERED", vendor.vendorId, `${vendor.vendorName} profile registered.`),
    ...vendor.capabilities.map((capability) =>
      createVendorDiagnostic("VENDOR_CAPABILITY_LINKED", vendor.vendorId, `${capability.capabilityName} linked.`, {
        capabilityId: capability.capabilityId,
        capabilityType: capability.capabilityType,
      }),
    ),
    ...vendor.assets.map((asset) =>
      createVendorDiagnostic("VENDOR_ASSET_LINKED", vendor.vendorId, `${asset.assetType} linked.`, {
        assetId: asset.assetId,
        ownershipType: asset.ownershipType,
      }),
    ),
    ...vendor.priceBooks.map((priceBookId) =>
      createVendorDiagnostic("VENDOR_PRICEBOOK_REGISTERED", vendor.vendorId, `${priceBookId} registered.`),
    ),
    ...vendor.serviceRegions.map((serviceRegion) =>
      createVendorDiagnostic("VENDOR_REGION_LINKED", vendor.vendorId, `${serviceRegion.name} linked.`, {
        serviceAreaId: serviceRegion.serviceAreaId,
        areaType: serviceRegion.areaType,
      }),
    ),
    createVendorDiagnostic("VENDOR_QUALIFICATION_RECORDED", vendor.vendorId, `${vendor.qualificationStatus} qualification recorded.`, {
      insuranceStatus: vendor.insuranceStatus,
    }),
  ]),
);

